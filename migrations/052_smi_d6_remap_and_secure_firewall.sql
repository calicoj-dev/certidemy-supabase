-- ---------------------------------------------------------------------------
-- (2) Secure firewall cleanup: drop question_concepts for SM-I secure questions.
--     Scoped to pool='secure' so practice links are never touched.
-- ---------------------------------------------------------------------------

delete from public.question_concepts qc
where qc.question_id in (
  select q.id
  from public.quiz_questions q
  join public.tasks t on t.id = q.task_id
  join public.domains d on d.id = t.domain_id
  where d.certification_id = '11111111-1111-1111-1111-111111111111'
    and q.pool = 'secure'
);

