-- Two changes, applied editor-first; this is the versioned record.
--
-- (1) SM-I D6 remap. SM-I carried a 6th domain "The Scrum Master with
--     AI-Augmented Teams" (D6) at weight 0.00 — AI was authored but OUT of exam
--     scope, ghettoized in its own domain/module. This re-homes the 7 AI tasks
--     into D1–D5 where the work belongs (the SPO-I "AI woven through every
--     domain" model), re-homes their 6 lessons into Modules 1/2/4/5, flips 6 of
--     the 7 tasks to is_exam_scope=true (all but the one with no secure pool),
--     and retires the empty D6 domain + Module 6. Net: 5 domains, 5 modules, an
--     AI exam task in every domain. No domain weights change (D6 was 0).
--
--     Task re-map (codes renumbered to fit new homes; questions key off task
--     UUID, not code, so renumbering is safe):
--       6.4 -> D1 1.7   (exam)   empiricism under AI acceleration
--       6.1 -> D2 2.10  (exam)   AI agents as tools; human accountabilities
--       6.5 -> D2 2.11  (exam)   delegable AI work vs retained accountabilities
--       6.6 -> D3 3.11  (exam)   AI signal as input to inspection
--       6.2 -> D4 4.11  (exam)   DoD + transparency for AI-generated work
--       6.3 -> D5 5.10  (exam)   new impediments in AI-augmented teams
--       6.7 -> D5 5.11  (NOT exam — no secure pool yet; fast-follow secure-gen)
--
-- (2) Secure firewall cleanup. The SM-I secure pool (1,077 question_concepts
--     links across D1–D5, all langs) was generated by a legacy pre-firewall
--     path that wrote question_concepts like practice items do. That makes
--     secure exam questions reachable from practice via fetchConceptPractice —
--     a leak. SPO-I (built with the hardened task-first generator) was clean (0
--     links), confirming this is SM-I-only legacy residue. We delete every
--     question_concepts row belonging to an SM-I secure question. Practice
--     links (pool='practice') are untouched. The old generator path is retired;
--     the hardened generator (SPO-I's) never writes question_concepts for
--     secure items.
--
-- Idempotent: each statement is guarded so a re-run is a no-op once applied.

